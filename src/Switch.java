public class Switch {
   public static void main(String[] args) {  
	   int num = 3;
       
       switch(num) {

       case 1:
           System.out.println ("1 입니다.");
           System.out.println ("1 입니다.");
           
       case 2:
           System.out.println ("2 입니다.");
           System.out.println ("2 입니다.");
           
       case 3:
           System.out.println ("3 입니다.");
           System.out.println ("3 입니다.");
           
       default:
           System.out.println ("디폴트 입니다.");
           System.out.println ("디폴트 입니다.");
       }
   }
}

